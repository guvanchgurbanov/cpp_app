#include <iostream>


using namespace std;
int main() {
    int a = 12;
    short b = 5;
    cout << a << '+' << b << '=' << a+b << endl;
    cout << a << '-' << b << '=' << a-b<< endl;
    cout << a << '/' << b << '=' << a/b<< endl;
    cout << a << '*' << b << '=' << a*b << endl;
    cout << a << '%' << b << '=' << a%b << endl;
    return 0;
}